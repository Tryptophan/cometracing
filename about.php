    <h1>About Formula SAE</h1>
    <p>The SAE International Formula SAE program is an engineering design competition
    for undergraduate and graduate students. The competition provides participants
    with the opportunity to enhance their engineering design and project management
    skills by applying learned classroom theories in a challenging competition. The
    engineering design goal for teams is to develop and construct a single-seat race car
    for the non-professional weekend autocross racer with the best overall package of design,
    construction, performance and cost.<br><br>The concept behind Formula SAE is that a fictional manufacturing
    company has contracted a design team to develop a small Formula-style race car.
    The prototype race car is to be evaluated for its potential as a production item.
    The target marketing group for the race car is the non-professional weekend autocross
    racer. Each student team designs, builds and tests a prototype based on a series of
    rules whose purpose is both to ensure onsite event operations and promote clever
    problem solving. The vehicle will be inspected in a series of tests to ensure it
    complies with the competition rules; in addition, the vehicle with driver will be
    judged in a number of performance tests on track. The rest of the judging is
    completed by experts from motorsports, automotive, aerospace and supplier
    industries on student design, cost and sales presentations.<br><br>Formula SAE
    promotes careers and excellence in engineering as it encompasses all aspects
    of the automotive industry including research, design,
    manufacturing, testing, developing, marketing, management and finances.
    </p>
